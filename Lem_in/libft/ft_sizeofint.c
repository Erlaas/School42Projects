#include "libft.h"

int		ft_sizeofint(int n)
{
	int		i;
	int		neg;

	if (n == -2147483648)
		return (11);
	neg = 0;
	i = 0;
	if (n < 0)
	{
		n = -n;
		neg = 1;
	}
	while (n >= 10)
	{
		n = n / 10;
		i++;
	}
	i++;
	return (i + neg);
}
