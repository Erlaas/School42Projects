#include "libft.h"

char	*ft_strrev(char *s1)
{
	int	i;
	int	j;

	if (!s1)
		return (NULL);
	i = 0;
	j = ft_strlen(s1) - 1;
	while (i < j)
	{
		ft_swap(&s1[i], &s1[j]);
		i++;
		j--;
	}
	return (s1);
}
